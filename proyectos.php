                        <br>
                        Los alumnos tienen derecho a que se los ayude a reflexionar por cuenta propia. 
                        Tienen derecho a que se les brinde espacios en los cuales poder expresarse libremente 
                        e intercambiar ideas, a partir del diálogo crítico y respetuoso con los demás. 
                        Promover  la realización de estos “derechos” es el motivo fundamental del presente proyecto.<br>
                        La articulación de asignaturas contribuirá al conocimiento y reflexión de la realidad 
                        para vivir de acuerdo a un conjunto de criterios (éticos, epistemológicos, sociales, 
                        políticos y culturales) que ordenen, regulen y orienten en mayor o menor medida la vida 
                        de los jóvenes. Es así que en un trabajo interdisciplinario, la Filosofía como 
                        disciplina formativa, que promueve la capacidad de reflexión, pensamiento crítico 
                        y creativo; junto con el aporte de Lengua Castellana para la utilización de estrategias 
                        lingüísticas, principalmente de la argumentación, para la expresión y defensa de ideas, 
                        los estudiantes tendrán la posibilidad de la reflexión sobre sí mismos y el mundo que 
                        los rodea, partiendo de la experiencia de su propia situación.<br>
                        <br>
                        Los jóvenes participan en las actividades como:<br>
